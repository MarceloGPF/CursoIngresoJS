function mostrar()
{
	let nombre = prompt("Ingrese su nombre:");// Declaro la variable nombre con let y usando el operador de asignación le asigno el valor obtenido por la función prompt.
	alert(nombre);// Muestro el valor asignado a la variable por el recuadro de la función alert.
}

