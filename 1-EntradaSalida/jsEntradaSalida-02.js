function mostrar()
{
	let nombre;// Declaro la variable nombre con la palabra reservada let.
	nombre = prompt("Ingrese su nombre:");
	/* Usando el operador de asignación =
	 le asigno el valor obtenido mediante la función prompt a la variable nombre.*/

	alert(nombre);// Muestro el valor asignado a la variable por el recuadro de la función alert.
}

