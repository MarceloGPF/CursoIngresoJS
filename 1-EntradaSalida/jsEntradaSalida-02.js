/*
Debemos lograr tomar un nombre con 'prompt' 
y luego mostrarlo por 'alert' al presionar el botón  'mostrar'*/
function mostrar()
{
	let nombre = prompt("Ingrese su nombre:");// Declaro la variable con let nombre y usando el operador de asignación le asigno un valor.
	alert(nombre);// Muestro el valor de la variable.
}

